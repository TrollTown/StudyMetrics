drop table Users;
drop table History;
drop table Teaches;
drop table Classes;
drop table Questions;
drop table Tags;
drop table Subjects;
drop table Modules;
drop table Submodules;

create table Users (
	id          serial,
	email       text,
	name		text,
	passwd		text,
    isTeacher   boolean,
	primary key (id)
);

create table Teaches (
    classID   integer,
    teacherID   integer,
    className   text,
    subjectID   integer,
    primary key (classID)
);

create table Classes (
    classID   integer,
    studentID   integer,
    primary key (classID,studentID)
);

create table History (
    questionID  integer,
    studentID   integer,
    finish_time timestamp,
    masteredQ   boolean,
    nextAttempt timestamp,
    student_answer   text,
    result      boolean,
    approved    boolean,
    primary key (questionID,studentID,finish_time)
);

create table Questions (
    questionID  serial,
    subjectID   integer,
    moduleID    integer,
    submoduleID integer,
    questionText    text,
    questionType    text, -- 'mc' / 'numeric' / 'whiteboard'
    answer      text,
    photo       text,
    difficulty  integer,
    authorID    integer,
    starred     boolean,
    primary key (questionID)
);

create table Tags (
    questionID integer,
    tagName     varchar(255),
    primary key (questionID, tagName)
);

create table Subjects (
    subjectID   serial,
    subjectName text,
    primary key (subjectID)
);

create table Modules (
    moduleID    serial,
    subjectID   integer,
    moduleName  text,
    primary key (moduleID)
);

create table Submodules (
    submoduleID serial,
    moduleID    integer,
    submoduleName text,
    primary key (submoduleID)
);


